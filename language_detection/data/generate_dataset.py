def generate_dataset(output_path: str, samples_per_language: int = 30):
    """Generate balanced multilingual dataset CSV."""
    rows = []
    for lang, sentences in LANGUAGE_DATA.items():
        pool = sentences.copy()
        # Augment by shuffling words slightly if needed
        while len(pool) < samples_per_language:
            s = random.choice(sentences)
            pool.append(s)
        selected = random.sample(pool, min(samples_per_language, len(pool)))
        for text in selected:
            rows.append({"Text": text, "Language": lang})

    random.shuffle(rows)
    with open(output_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=["Text", "Language"])
        writer.writeheader()
        writer.writerows(rows)
    print(f"Dataset saved: {output_path} ({len(rows)} rows, {len(LANGUAGE_DATA)} languages)")
    return output_path


if __name__ == "__main__":
    generate_dataset("dataset.csv")
